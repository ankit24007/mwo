                                 <b>Verified By Visa - MasterCard Secure Code</b>
<p>The RBI has made it mandatory, from 1st August 2009 onwards, for all online transactions to have an extra level of authentication to make online shopping safer. The 'extra' level is a password that you will have to enter after entering your credit/debit card details while making online payments. You will require this for transacting on any website in India, including MovieWorldOnline. This new technology is called VBV - Verified by Visa or MSC - MasterCard Secure Code</p>

<b>How do I get this 'extra password'?</b>
<p>It's simple. Log on to your bank's website, register your card for Verified by Visa or MasterCard SecureCode and get your password. Each bank has its own process for card registration so log on to your bank's website for details.</p>

<b>What do I do after registering with VBV/MSC?</b>
<p>Now, every time you book on MovieWorldOnline, after entering your card details on the payment page, you will be redirected to your bank's website. Enter your VBV/MSC password thereafter which you will be redirected back to MovieWorldOnline for completing the booking process.</p>

<b>Do American Express cards have a similar authentication?</b>
<p>For Amex cards the 'extra level of authentication' will work slightly differently. Amex uses the AVS-Address Verification System. On the payment page on MovieWorldOnline, you will be asked to enter your billing address. This will be checked against your billing address the bank has on its records. If the addresses don't match your payment will be rejected. If you don't remember your exact billing address, you can confirm it from your Credit Card statement.</p>

<b>What if my bank does not support VBV/MSC?</b>
<p>The RBI notification concerns cards issued within India only. If your card is issued outside India, your online transactions will go through even without this extra authentication step (unless your Card Issuer requires VBV/MSC authentication). If your card is issued in India and does not support VBV/MSC, we suggest you look for a new bank.</p>

